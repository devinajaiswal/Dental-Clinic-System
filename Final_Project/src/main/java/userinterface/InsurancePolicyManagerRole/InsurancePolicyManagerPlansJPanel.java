    private void populateTable(ArrayList<InsurancePlan> plans) {
        DefaultTableModel model = (DefaultTableModel) tablePlan.getModel();

        model.setRowCount(0);
        for (InsurancePlan plan : plans) {
            Object[] row = new Object[5];
            row[0] = plan;
            row[1] = plan.getPrice();
            row[2] = plan.getFillingCoverage();
            row[3] = plan.getRootCoverage();
            row[4] = plan.getSrpCoverage();
            model.addRow(row);
        }

    }

