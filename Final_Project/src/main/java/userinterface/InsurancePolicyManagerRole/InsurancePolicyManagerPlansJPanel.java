    private void resetInternalFrame() {
        setComponentsBorderBlack();
        txtName.setText("");
        txtPrice.setText("");
        txtFilling.setText("");
        txtRoot.setText("");
        txtSRP.setText("");
        jInternalFrame1.setVisible(false);
        currentAction = null;
        tablePlan.setEnabled(true);
    }

    private void setButtonsEnabled(boolean enable) {
        buttonAddNew.setEnabled(enable);
        buttonEdit.setEnabled(enable);
    }

    private void setComponentsBorderBlack() {
        userinterface.Util.setBorderBlack(txtFilling, txtPrice, txtName, txtRoot, txtSRP);
    }


    private void buttonConfirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonConfirmActionPerformed
        if (!userinterface.Util.requireDouble(this, txtFilling, txtPrice, txtSRP, txtRoot)) {
            return;
        }
        if (!userinterface.Util.requireNotEmpty(this, txtName)) {
            return;
        }
        setComponentsBorderBlack();

        if (currentAction.equals(ACTION_ADD)) {
            InsurancePlan plan = new InsurancePlan();
            plan.setPlanName(txtName.getText());
            plan.setPrice(Double.parseDouble(txtPrice.getText()));
            plan.setFillingCoverage(Double.parseDouble(txtFilling.getText()));
            plan.setRootCoverage(Double.parseDouble(txtRoot.getText()));
            plan.setSrpCoverage(Double.parseDouble(txtSRP.getText()));
            data.PricesDAO.createPlan(enterprise.getEnterpriseId(), plan);
            JOptionPane.showMessageDialog(this, "Created successfully!");
        } else if (currentAction.equals(ACTION_EDIT)) {
            InsurancePlan plan = (InsurancePlan) tablePlan.getValueAt(tablePlan.getSelectedRow(), 0);
            plan.setPlanName(txtName.getText());
            plan.setPrice(Double.parseDouble(txtPrice.getText()));
            plan.setFillingCoverage(Double.parseDouble(txtFilling.getText()));
            plan.setRootCoverage(Double.parseDouble(txtRoot.getText()));
            plan.setSrpCoverage(Double.parseDouble(txtSRP.getText()));
            data.PricesDAO.updatePlan(plan);
            JOptionPane.showMessageDialog(this, "Updated successfully!");
        }
        populateTable();
        resetInternalFrame();
        setButtonsEnabled(true);
    }//GEN-LAST:event_buttonConfirmActionPerformed

    private void buttonCancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCancelActionPerformed
        resetInternalFrame();
        jInternalFrame1.setVisible(false);
        setButtonsEnabled(true);
        tablePlan.setEnabled(true);
    }//GEN-LAST:event_buttonCancelActionPerformed

    private void buttonAddNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAddNewActionPerformed
        jInternalFrame1.setVisible(true);
        currentAction = ACTION_ADD;
        setButtonsEnabled(false);
        tablePlan.setEnabled(false);
    }//GEN-LAST:event_buttonAddNewActionPerformed

    private void buttonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonEditActionPerformed
        if (tablePlan.getSelectedRow() >= 0) {
            InsurancePlan plan = (InsurancePlan) tablePlan.getValueAt(tablePlan.getSelectedRow(), 0);
            txtFilling.setText(plan.getFillingCoverage() + "");
            txtName.setText(plan.getPlanName());
            txtSRP.setText(plan.getSrpCoverage() + "");
            txtPrice.setText(plan.getPrice() + "");
            txtRoot.setText(plan.getRootCoverage() + "");

            currentAction = ACTION_EDIT;
            jInternalFrame1.setVisible(true);
            setButtonsEnabled(false);
            tablePlan.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(this, "Please select a record first");
            return;
        }
    }//GEN-LAST:event_buttonEditActionPerformed

