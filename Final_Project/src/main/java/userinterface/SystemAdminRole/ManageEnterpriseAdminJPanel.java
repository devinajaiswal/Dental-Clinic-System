    private void populateTable() {
        DefaultTableModel model = (DefaultTableModel) tableAdmin.getModel();

        model.setRowCount(0);
        for (UserAccount userAccount : data.UserDAO.getAllEnterpriseAdmin()) {
            Object[] row = new Object[3];
            row[0] = userAccount.getEnterprise().getEnterpriseName();
            row[1] = userAccount.getEnterprise().getNetwork().getName();
            row[2] = userAccount;
            model.addRow(row);
        }
    }

    private void populateNetworkComboBox() {
        comboNetwork.removeAllItems();
        for (Network network : data.NetworkDAO.getAll()) {
            comboNetwork.addItem(network);
        }
    }

    private void populateEnterpriseComboBox(Network network) {
        comboEnterprise.removeAllItems();

        for (Enterprise enterprise : data.EnterpriseDAO.getAllbyNetworkName(network.getName())) {
            comboEnterprise.addItem(enterprise);
        }

    }

